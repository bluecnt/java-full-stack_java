// 주석(comment) : 프로그램 실행에 영향을 미치지 않는 글자들
/*
	한번에 여러 줄을 주석 처리하는 주석
*/

public class A00_Hello2 {

	// main() : 프로그램 그 자체
	//			메인이 시작되면 프로그램이 시작되고
	//			메인 끝나면 프로그램이 끝난다
	public static void main(String[] args) {
		// 프로그램의 시작
		
		// # System.out.println
		
		// - ()에 전달한 값을 콘솔 화면에 출력해라
		// - 콘솔 : 글자만 나오는 옛날 컴퓨터 화면		
		
		// ;(세미콜론, semi-colon)
		// - 한 명령어가 끝났음을 알리는 문자
		System.out.println("Hello, World!");
		System.out.println("김밥");
		System.out.println("참치김밥");
		System.out.println("돈가스김밥");
		
		// 프로그램의 끝
	}
}

